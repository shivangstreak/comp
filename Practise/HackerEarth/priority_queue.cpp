#include<bits/stdc++.h>
using namespace std;
int main(){
	int a[]={1,2,3,4};
	priority_queue<int,vector<int>,greater<int>>q(a,a+4); // This is min heap;	
	while(!q.empty()){								// To use any other data type use that type in place of int
		cout<<q.top()<<" ";
		q.pop();
	}
	cout<<endl;
	return 0;
}
